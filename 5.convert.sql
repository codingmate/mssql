DECLARE @STR VARCHAR(100) = '2022-07-30 15:05:12'

SELECT CONVERT( DATETIME, @STR )
     , CONVERT( VARCHAR(10), @STR, 120 ) -- 2022-07-20
     , CONVERT( VARCHAR(7), @STR, 120 ) -- 2022-07
     
     , CONVERT(VARCHAR, GETDATE(), 120) -- 2022-08-03 15:40:00
     , CONVERT(VARCHAR(7), GETDATE(), 120) -- 2022-08
     , CONVERT(VARCHAR(10), GETDATE(), 120) -- 2022-08-03
     , CONVERT(VARCHAR, GETDATE(), 121) -- 2022-08-03 15:40:00.073
     , CONVERT(VARCHAR(7), GETDATE(), 121) -- 2022-08
     , CONVERT(VARCHAR(10), GETDATE(), 121) -- 2022-08-03
     
     --, CONVERT(TIME, GETDATE()) -- 2022-08-03 / TOO LATE
     , CONVERT(TIME, GETDATE()) -- 15:40:00.073000
     , CONVERT(VARCHAR, GETDATE(), 8) -- 15:26:00
     , CONVERT(VARCHAR, GETDATE(), 14) -- 15:26:00:073
